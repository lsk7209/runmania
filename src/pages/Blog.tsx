/* ─── DB → BlogPost mapper ─── */

const mapDbPostToLocal = (dbPost: any): BlogPost => ({
  slug: dbPost.slug,
  title: dbPost.title,
  excerpt: dbPost.excerpt || "",
  content: Array.isArray(dbPost.content) ? dbPost.content as string[] : [],
  tags: dbPost.tags || [],
  readTime: dbPost.read_time || "10분",
  date: dbPost.published_at ? dbPost.published_at.split("T")[0] : dbPost.created_at?.split("T")[0] || "",
  dateModified: dbPost.updated_at?.split("T")[0] || "",
  heroImage: HERO_IMAGE_MAP[dbPost.hero_image] || dbPost.hero_image || nb1080,
  relatedSlugs: dbPost.related_slugs || [],
  faq: Array.isArray(dbPost.faq) ? dbPost.faq as FaqItem[] : [],
});

/* ─── Custom Hook: Fetch posts from DB with local fallback ─── */

const useBlogPosts = () => {
  const [posts, setPosts] = useState<BlogPost[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchPosts = async () => {
      try {
        const response = await fetch("/api/posts");
        if (!response.ok) throw new Error("Fetch failed");
        let data = await response.json();

        if (!data || data.length === 0) {
          setPosts(LOCAL_POSTS);
        } else {
          setPosts(data.map(mapDbPostToLocal));
        }
      } catch {
        setPosts(LOCAL_POSTS);
      } finally {
        setLoading(false);
      }
    };
    fetchPosts();
  }, []);

  return { posts, loading };
};

