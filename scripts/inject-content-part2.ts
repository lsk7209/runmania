async function main() {
  console.log("🚀 블로그 포스트 11~20번 주입 시작\n");
  let success = 0;
  let failed = 0;

  for (const post of POSTS) {
    const postId = crypto.randomUUID();
    const publishDate = scheduledAt(post.dayOffset);

    console.log(`[${post.dayOffset - 9}/10] ${post.title.slice(0, 40)}...`);
    console.log(`       발행 예정: ${publishDate.slice(0, 10)} KST`);

    try {
      const existing = await db.execute({
        sql: "SELECT id FROM blog_posts WHERE slug = ?",
        args: [post.slug],
      });

      let targetId: string;
      if (existing.rows.length > 0) {
        targetId = String(existing.rows[0].id);
        await db.execute({
          sql: "UPDATE blog_posts SET scheduled_at=?, updated_at=datetime('now') WHERE id=?",
          args: [publishDate, targetId],
        });
      } else {
        targetId = postId;
        await db.execute({
          sql: `INSERT INTO blog_posts
            (id, title, slug, excerpt, content, tags, read_time, hero_image,
             related_slugs, faq, status, content_type, workflow_status,
             generation_meta, scheduled_at, created_at, updated_at)
            VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,datetime('now'),datetime('now'))`,
          args: [
            targetId,
            post.title,
            post.slug,
            post.excerpt,
            JSON.stringify(post.content),
            JSON.stringify(post.tags),
            post.read_time,
            post.hero,
            "[]",
            JSON.stringify(post.faq),
            "scheduled",
            "blog",
            "approved",
            "{}",
            publishDate,
          ],
        });
      }

      // 콘텐츠 업데이트
      await db.execute({
        sql: `UPDATE blog_posts SET
          excerpt=?, content=?, tags=?, read_time=?, faq=?,
          status='scheduled', workflow_status='approved',
          last_generated_at=datetime('now'), generation_count=1,
          updated_at=datetime('now')
          WHERE id=?`,
        args: [
          post.excerpt,
          JSON.stringify(post.content),
          JSON.stringify(post.tags),
          post.read_time,
          JSON.stringify(post.faq),
          targetId,
        ],
      });

      console.log(`       ✅ 완료\n`);
      success++;
    } catch (err) {
      console.error(`       ❌ 실패: ${(err as Error).message}\n`);
      failed++;
    }
  }

  console.log("─".repeat(50));
  console.log(`완료: 성공 ${success}개 / 실패 ${failed}개`);
  console.log("블로그 총 20개 scheduled 상태로 등록 완료");
}

main().catch((e) => {
  console.error("Fatal:", e);
  process.exit(1);
});
