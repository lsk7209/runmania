async function main() {
  console.log("🚀 10개 포스트 콘텐츠 직접 주입 시작\n");
  let success = 0;
  let failed = 0;

  for (const post of POSTS) {
    try {
      const result = await db.execute({
        sql: `UPDATE blog_posts SET
          excerpt=?, content=?, tags=?, read_time=?, faq=?,
          status='scheduled', workflow_status='approved',
          last_generated_at=datetime('now'), generation_count=1,
          updated_at=datetime('now')
          WHERE slug=?`,
        args: [
          post.excerpt,
          JSON.stringify(post.content),
          JSON.stringify(post.tags),
          post.read_time,
          JSON.stringify(post.faq),
          post.slug,
        ],
      });
      if (result.rowsAffected > 0) {
        console.log(`✅ ${post.slug}`);
        success++;
      } else {
        console.log(`⚠️  미존재 (스킵): ${post.slug}`);
      }
    } catch (err) {
      console.error(`❌ ${post.slug}: ${(err as Error).message}`);
      failed++;
    }
  }

  console.log(`\n완료: 성공 ${success}개 / 실패 ${failed}개`);
  console.log("✅ status=scheduled, workflow_status=approved 설정 완료");
  console.log("→ GitHub Actions cron이 scheduled_at 도래 시 자동 발행합니다");
}

main().catch((e) => {
  console.error("Fatal:", e);
  process.exit(1);
});
